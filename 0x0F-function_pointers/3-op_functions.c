#include "3-calc.h"
/**
 * op_add- sum
 * @a: Integer
 * @b: Integer
 *
 * Return: Integer
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub- difference
 * @a: Integer
 * @b: Integer
 *
 * Return: Integer
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul- product
 * @a: Integer
 * @b: Integer
 *
 * Return: Integer
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div- division
 * @a: Integer
 * @b: Integer
 *
 * Return: Integer
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod- remainder of division
 * @a: Integer
 * @b: integer
 *
 * Return: Integer
 */
int op_mod(int a, int b)
{
	return (a % b);
}

